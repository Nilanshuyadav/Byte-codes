class Solution {
    
    public void Rearrange(int a[], int n)
    {
        // Your code goes here
        int i=0, j=n-1;
        int temp;
        
        for(int ind=0; ind<n; ind++){
            if(a[ind]<0){
                if(ind == i){
                    i++;
                    continue;
                }
                
                temp = a[ind];
                j=ind;
                while(i<j){
                    a[j] = a[j-1];
                    j--;
                }
                
                a[i++] = temp;
            }
        }
        
    }
}