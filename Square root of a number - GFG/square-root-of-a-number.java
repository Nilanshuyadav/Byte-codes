// Function to find square root
// x: element to find square root
class Solution
{
     long floorSqrt(long x)
	 {
		// Your code here
		double pow = Math.log(x)/2;
		
		long ans = (long)(Math.pow(Math.exp(1), pow)+1);
		return ans*ans == x ? ans : ans-1;
	 }
}
